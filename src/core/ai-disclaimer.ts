/**
 * AI Disclaimer Framework (S12).
 *
 * Centralised module for all AI-related disclaimers, consent tracking,
 * and disclosure banners. Every AI feature (WebLLM, NLP sentiment,
 * pattern recognition, anomaly detection) MUST route through this
 * module before rendering AI-generated content.
 *
 * Principles:
 *   1. Informed consent — user acknowledges AI limitations before use
 *   2. Persistent disclosure — visible label on every AI-generated element
 *   3. Opt-in activation — AI features default to off until user enables
 *   4. No financial advice — every AI output carries a disclaimer
 */

// ── Types ─────────────────────────────────────────────────────────────────

/** Categories of AI features that require disclaimers. */
export type AiFeatureId =
  | "webllm"
  | "nlp-sentiment"
  | "pattern-recognition"
  | "anomaly-detection"
  | "screener-dsl-translate"
  | "signal-dsl-translate"
  | "chart-explanation";

export interface AiDisclaimerConfig {
  /** Feature identifier. */
  readonly feature: AiFeatureId;
  /** Short label shown next to AI-generated content (e.g. "AI-generated"). */
  readonly badge: string;
  /** Full disclaimer text for the consent dialog. */
  readonly disclaimer: string;
  /** Whether the feature requires explicit opt-in (default: true). */
  readonly requiresOptIn: boolean;
}

export interface AiConsentState {
  /** Which features the user has acknowledged. */
  readonly acknowledged: ReadonlySet<AiFeatureId>;
  /** ISO timestamp of last acknowledgement. */
  readonly lastAcknowledgedAt: string | null;
}

