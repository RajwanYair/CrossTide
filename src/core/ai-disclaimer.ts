// ── Storage ───────────────────────────────────────────────────────────────

const STORAGE_KEY = "ct-ai-consent";

function loadConsent(): AiConsentState {
  try {
    const storage = typeof localStorage !== "undefined" ? localStorage : null;
    const raw = storage?.getItem(STORAGE_KEY) ?? null;
    if (!raw) return { acknowledged: new Set(), lastAcknowledgedAt: null };
    const parsed = JSON.parse(raw) as {
      acknowledged?: string[];
      lastAcknowledgedAt?: string;
    };
    return {
      acknowledged: new Set((parsed.acknowledged ?? []) as AiFeatureId[]),
      lastAcknowledgedAt: parsed.lastAcknowledgedAt ?? null,
    };
  } catch {
    return { acknowledged: new Set(), lastAcknowledgedAt: null };
  }
}

function saveConsent(state: AiConsentState): void {
  try {
    const storage = typeof localStorage !== "undefined" ? localStorage : null;
    storage?.setItem(
      STORAGE_KEY,
      JSON.stringify({
        acknowledged: [...state.acknowledged],
        lastAcknowledgedAt: state.lastAcknowledgedAt,
      }),
    );
  } catch {
    /* storage unavailable */
  }
}

// ── API ───────────────────────────────────────────────────────────────────

let _state: AiConsentState | null = null;

function getState(): AiConsentState {
  if (!_state) _state = loadConsent();
  return _state;
}

/** Check whether the user has acknowledged the disclaimer for a feature. */
export function hasConsent(feature: AiFeatureId): boolean {
  return getState().acknowledged.has(feature);
}

/** Record that the user has acknowledged the disclaimer for a feature. */
export function grantConsent(feature: AiFeatureId): void {
  const state = getState();
  const next: AiConsentState = {
    acknowledged: new Set([...state.acknowledged, feature]),
    lastAcknowledgedAt: new Date().toISOString(),
  };
  _state = next;
  saveConsent(next);
}

/** Revoke consent for a specific feature (opt-out). */
export function revokeConsent(feature: AiFeatureId): void {
  const state = getState();
  const ack = new Set(state.acknowledged);
  ack.delete(feature);
  const next: AiConsentState = {
    acknowledged: ack,
    lastAcknowledgedAt: state.lastAcknowledgedAt,
  };
  _state = next;
  saveConsent(next);
}

/** Revoke all AI consents. */
export function revokeAllConsent(): void {
  _state = { acknowledged: new Set(), lastAcknowledgedAt: null };
  saveConsent(_state);
}

/** Get the disclaimer config for a feature. */
export function getDisclaimer(feature: AiFeatureId): AiDisclaimerConfig {
  const found = DEFAULT_DISCLAIMERS.find((d) => d.feature === feature);
  if (!found) throw new Error(`Unknown AI feature: "${feature}"`);
  return found;
}

/** Get all feature IDs that the user has acknowledged. */
export function getAcknowledgedFeatures(): readonly AiFeatureId[] {
  return [...getState().acknowledged];
}

// ── DOM Helpers ───────────────────────────────────────────────────────────

/**
 * Create an AI badge element to attach to AI-generated content.
 * Renders as a small inline label: "⚠️ AI-generated" (or feature-specific text).
 */
export function createAiBadge(feature: AiFeatureId): HTMLElement {
  const config = getDisclaimer(feature);
  const badge = document.createElement("span");
  badge.className = "ct-ai-badge";
  badge.setAttribute("role", "status");
  badge.setAttribute("aria-label", `${config.badge} — not financial advice`);
  badge.setAttribute("title", config.disclaimer.split("\n")[0]!);
  badge.textContent = `⚠️ ${config.badge}`;
  return badge;
}

/**
 * Wrap AI-generated content in a container with a visible disclaimer badge
 * and aria attributes for screen readers.
 */
export function wrapWithDisclaimer(content: HTMLElement, feature: AiFeatureId): HTMLElement {
  const wrapper = document.createElement("div");
  wrapper.className = "ct-ai-content";
  wrapper.setAttribute("data-ai-feature", feature);
  wrapper.setAttribute("role", "region");
  wrapper.setAttribute("aria-label", `AI-generated content: ${getDisclaimer(feature).badge}`);

  wrapper.appendChild(createAiBadge(feature));
  wrapper.appendChild(content);

  return wrapper;
}

/**
 * Guard function: ensures the user has consented before running an AI feature.
 * Returns true if consent exists; otherwise returns false (caller should show dialog).
 */
export function requireConsent(feature: AiFeatureId): boolean {
  const config = getDisclaimer(feature);
  if (!config.requiresOptIn) return true;
  return hasConsent(feature);
}

/** Reset internal state (for testing). */
export function _resetForTesting(): void {
  _state = null;
}
